/*
Aritméticos
+ adição / Concatenação ++ or --
- diminuir / dividir * vezes 
*/

const num1 = "5"; // String
const num2 = parseInt("5.5"); //NumInt
const num3 = parseFloat("5.5"); //NumFloat
const num4 = 5; //Num
const num5 = 10; //Num
console.log(num1 + num5);
console.log(num2 + num5);
console.log(num3 + num5);
console.log(num4 + num5);

// Contador
let cont = 0;
++cont;
cont++;
--cont;
cont--;
++cont;
console.log(cont);
console.log(typeof cont);
